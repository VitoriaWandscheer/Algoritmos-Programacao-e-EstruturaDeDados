# Você deverá armazenar dois números em variáveis,
# realize a soma dos valores, a subtração e a divisão.
# Utilize as variáveis necessárias.

a = float(input('Informe o primeiro número: '))
b = float(input('Agora o segundo número: '))

soma = a + b
sub = a - b
div = a / b

print(f'A soma dos números {a} e {b} é igual a {soma}')
print(f'A diferença entre {a} e {b} é igual a {sub}')
print(f'A razão entre {a} e {b} é igual a {div}')

print('=)')