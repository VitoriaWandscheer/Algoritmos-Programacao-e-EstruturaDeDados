
# Leia a quantidade de cavalos que um haras possui e faça um algoritmo para calcular
# quantas ferraduras são necessárias para equipar todos os cavalos do haras.

cavalo = int(input('Informe a quantidade de cavalos: '))

FERR_CAVALO = 4

tot_ferr = cavalo * FERR_CAVALO

print (f'São necessárias {tot_ferr} ferraduras para equipar todos os cavalos do haras.')

