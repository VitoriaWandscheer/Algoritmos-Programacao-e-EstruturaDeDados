# Faça um algoritmo que calcule a área de um terreno.
# Os terrenos são retangulares,
# e deverão ser informadas as medidas de largura e comprimento do terreno.

larg = float(input('Digite o valor da largura do terreno: '))
comp = float(input('Digite o valor do comprimento do terreno: '))

area = larg * comp

print (f'O terreno possuí {area:.3f} metros quadrados')

print('=)')